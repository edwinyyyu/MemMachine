# Oversample factor: dual-view doubles the index, so to be safe fetch 2x K
# at the vector-search layer before deduping by turn_id.
OVERSAMPLE = 2


@dataclass
class DualEMHit:
    """Like EMHit but tags which view produced the score."""

    turn_id: int
    score: float
    seed_segment_uuid: object
    role: str
    text: str
    view: str  # "raw" or "summary" or "unknown"


def load_questions() -> list[dict]:
    with open(DATA_DIR / "questions_extended.json") as f:
        qs = json.load(f)
    return [q for q in qs if q.get("benchmark") == "locomo"][:30]


def load_collections_meta() -> dict:
    with open(RESULTS_DIR / "eventmemory_summ_collections.json") as f:
        return json.load(f)


def compute_recall(retrieved: set[int], gold: set[int]) -> float:
    if not gold:
        return 1.0
    return len(retrieved & gold) / len(gold)


async def _query_em_dual(
    memory: EventMemory,
    text: str,
    *,
    vector_search_limit: int,
    expand_context: int = 0,
    property_filter=None,
) -> list[DualEMHit]:
    """Run EM.query and flatten into DualEMHit with view tag from segment
    properties. Multi-view ingest stores `view` on each event's properties.
    """
    qr = await memory.query(
        query=text,
        vector_search_limit=vector_search_limit,
        expand_context=expand_context,
        property_filter=property_filter,
    )
    hits: list[DualEMHit] = []
    for sc in qr.scored_segment_contexts:
        for seg in sc.segments:
            view = str(seg.properties.get("view", "unknown"))
            hits.append(
                DualEMHit(
                    turn_id=int(seg.properties.get("turn_id", -1)),
                    score=sc.score,
                    seed_segment_uuid=sc.seed_segment_uuid,
                    role=str(seg.properties.get("role", "")),
                    text=seg.block.text,
                    view=view,
                )
            )
    return hits


def _dedupe_dual_by_turn_id(hits: list[DualEMHit]) -> list[DualEMHit]:
    """First occurrence wins (highest score since the underlying list is
    already sorted by score from the vector store)."""
    seen: set[int] = set()
    out: list[DualEMHit] = []
    for h in hits:
        if h.turn_id in seen:
            continue
        seen.add(h.turn_id)
        out.append(h)
    return out


def _merge_dual_by_max_score(
    batches: list[list[DualEMHit]],
) -> list[DualEMHit]:
    best: dict[int, DualEMHit] = {}
    for batch in batches:
        seen_in_batch: set[int] = set()
        for h in batch:
            if h.turn_id in seen_in_batch:
                continue
            seen_in_batch.add(h.turn_id)
            prev = best.get(h.turn_id)
            if prev is None or h.score > prev.score:
                best[h.turn_id] = h
    return sorted(best.values(), key=lambda h: -h.score)


def _view_coverage(hits: list[DualEMHit], gold: set[int], K: int) -> dict:
    """For each gold turn found in top-K hits, record which view produced
    the winning entry."""
    topk = hits[:K]
    per_turn: dict[int, str] = {}
    for h in topk:
        if h.turn_id in gold and h.turn_id not in per_turn:
            per_turn[h.turn_id] = h.view
    return {str(tid): per_turn[tid] for tid in sorted(per_turn.keys())}


