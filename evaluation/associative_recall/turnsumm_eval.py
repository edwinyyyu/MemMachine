"""Dual-view (raw + summary) EventMemory evaluation on LoCoMo-30.

Variants:
  em_cosine_baseline_summ    -- raw-query cosine on dual-view index, dedupe turn_id
  em_v2f_summ                -- v2f cues on dual-view index
  em_v2f_summ_sf             -- v2f_speakerformat cues on dual-view index
  em_v2f_summ_sf_spkfilter   -- em_v2f_summ_sf + property_filter (speaker_filter)

The dual-view index has TWO entries per turn (raw + summary) sharing the
same `turn_id` property. `_dedupe_by_turn_id` and `_merge_by_max_score`
already dedupe by turn_id, so dual-view is subsumed by existing merge
logic; the only change is a larger `vector_search_limit` since duplicate
turn_ids consume slots.

View-coverage analysis: for each gold turn, track which view's segment
produced the top score per query, to quantify whether the summary view
provides orthogonal rescue value over the raw view alone.

Reads collections from results/eventmemory_summ_collections.json
(generated by em_setup_summ.py).

Outputs:
  results/turn_summary.json
  results/turn_summary.md
"""

