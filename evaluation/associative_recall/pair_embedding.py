class PairEmbeddingCache(EmbeddingCache):
    """Reads shared embedding caches, writes to dedicated pair file."""

    def __init__(self) -> None:
        self.cache_dir = CACHE_DIR
        self.cache_dir.mkdir(parents=True, exist_ok=True)
        self._cache: dict[str, list[float]] = {}
        for name in _SHARED_EMB_READ:
            p = self.cache_dir / name
            if not p.exists():
                continue
            try:
                with open(p) as f:
                    self._cache.update(json.load(f))
            except (json.JSONDecodeError, OSError):
                continue
        self.cache_file = _PAIR_EMB_FILE
        self._new_entries: dict[str, list[float]] = {}

    def put(self, text: str, embedding: np.ndarray) -> None:
        key = self._key(text)
        self._cache[key] = embedding.tolist()
        self._new_entries[key] = embedding.tolist()

    def save(self) -> None:
        if not self._new_entries:
            return
        existing: dict[str, list[float]] = {}
        if self.cache_file.exists():
            try:
                with open(self.cache_file) as f:
                    existing = json.load(f)
            except (json.JSONDecodeError, OSError):
                existing = {}
        existing.update(self._new_entries)
        tmp = self.cache_file.with_suffix(".json.tmp")
        with open(tmp, "w") as f:
            json.dump(existing, f)
        tmp.replace(self.cache_file)
        self._new_entries = {}


class PairLLMCache(LLMCache):
    """Reads shared LLM caches, writes to dedicated pair file."""

    def __init__(self) -> None:
        self.cache_dir = CACHE_DIR
        self.cache_dir.mkdir(parents=True, exist_ok=True)
        self._cache: dict[str, str] = {}
        for name in _SHARED_LLM_READ:
            p = self.cache_dir / name
            if not p.exists():
                continue
            try:
                with open(p) as f:
                    data = json.load(f)
            except (json.JSONDecodeError, OSError):
                continue
            for k, v in data.items():
                if v:
                    self._cache[k] = v
        self.cache_file = _PAIR_LLM_FILE
        self._new_entries: dict[str, str] = {}

    def put(self, model: str, prompt: str, response: str) -> None:
        key = self._key(model, prompt)
        self._cache[key] = response
        self._new_entries[key] = response

    def save(self) -> None:
        if not self._new_entries:
            return
        existing: dict[str, str] = {}
        if self.cache_file.exists():
            try:
                with open(self.cache_file) as f:
                    existing = json.load(f)
            except (json.JSONDecodeError, OSError):
                existing = {}
        existing.update(self._new_entries)
        tmp = self.cache_file.with_suffix(".json.tmp")
        with open(tmp, "w") as f:
            json.dump(existing, f)
        tmp.replace(self.cache_file)
        self._new_entries = {}


# ---------------------------------------------------------------------------
# Pair index — built per SegmentStore, cached across arch instances
# ---------------------------------------------------------------------------


def _cos_matrix(normalized_mat: np.ndarray, query_vec: np.ndarray) -> np.ndarray:
    qn = query_vec / max(float(np.linalg.norm(query_vec)), 1e-10)
    return normalized_mat @ qn


class PairIndex:
    """Secondary index of (user_turn, assistant_turn) pair embeddings.

    For each adjacent (user@t, assistant@t+1) pair in a conversation (and
    also (assistant@t, user@t+1) — "reply followed by next question"), build
    a single embedding of the concatenated text. Store `pair_idx -> (seg_a,
    seg_b)` so a pair-hit promotes both constituent turns.
    """

    def __init__(
        self,
        store: SegmentStore,
        embed_fn,
        use_role_prefix: bool = True,
        include_reverse: bool = False,
    ):
        """Build the pair index.

        Args:
            store: SegmentStore to walk.
            embed_fn: callable(text) -> np.ndarray (cached).
            use_role_prefix: if True, "USER: ...\\nASSISTANT: ..." else
                join with " ".
            include_reverse: if True, also pair (assistant@t, user@t+1).
                Default False — the canonical Q-A unit is (user, assistant).
        """
        self.store = store
        self.use_role_prefix = use_role_prefix
        self.include_reverse = include_reverse

        # pair_idx -> (conversation_id, seg_index_a, seg_index_b, pair_text)
        self.pairs: list[tuple[str, int, int, str]] = []

        # Gather segments per conversation, sorted by turn_id
        by_conv: dict[str, list[Segment]] = defaultdict(list)
        for seg in store.segments:
            by_conv[seg.conversation_id].append(seg)
        for cid in by_conv:
            by_conv[cid].sort(key=lambda s: s.turn_id)

        for cid, segs in by_conv.items():
            for k in range(len(segs) - 1):
                a = segs[k]
                b = segs[k + 1]
                # Must be adjacent in turn_id
                if b.turn_id != a.turn_id + 1:
                    continue
                # Build pair only if roles alternate user->assistant
                forward = a.role == "user" and b.role == "assistant"
                reverse = (
                    include_reverse
                    and a.role == "assistant"
                    and b.role == "user"
                )
                if not (forward or reverse):
                    continue
                if use_role_prefix:
                    prefix_a = "USER" if a.role == "user" else "ASSISTANT"
                    prefix_b = "USER" if b.role == "user" else "ASSISTANT"
                    pair_text = (
                        f"{prefix_a}: {a.text}\n{prefix_b}: {b.text}"
                    )
                else:
                    pair_text = f"{a.text} {b.text}"
                self.pairs.append((cid, a.index, b.index, pair_text))

        # Compute embeddings via provided embed_fn (so caching works).
        embeddings: list[np.ndarray] = []
        for i, (_cid, _ia, _ib, text) in enumerate(self.pairs):
            embeddings.append(embed_fn(text))
        if embeddings:
            mat = np.stack(embeddings).astype(np.float32)
        else:
            mat = np.zeros((0, 1536), dtype=np.float32)
        norms = np.linalg.norm(mat, axis=1, keepdims=True)
        norms = np.maximum(norms, 1e-10)
        self.embeddings = mat
        self.normalized_embeddings = mat / norms
        self.conversation_ids = np.array(
            [p[0] for p in self.pairs], dtype=object
        )

    def __len__(self) -> int:
        return len(self.pairs)

    def search(
        self,
        query_embedding: np.ndarray,
        top_m: int,
        conversation_id: str | None = None,
    ) -> list[tuple[int, float]]:
        """Return top-M (pair_idx, cosine) filtered to conversation."""
        if len(self.pairs) == 0:
            return []
        sims = _cos_matrix(self.normalized_embeddings, query_embedding)
        if conversation_id is not None:
            mask = self.conversation_ids == conversation_id
            sims = np.where(mask, sims, -1.0)
        top_idx = np.argsort(sims)[::-1][:top_m]
        return [
            (int(i), float(sims[i])) for i in top_idx if sims[i] > -1.0
        ]


# Module-level per-store cache so each arch variant with the SAME prefix
# setting reuses the same built index within a process.
_PAIR_INDEX_REGISTRY: dict[tuple[int, bool, bool], "PairIndex"] = {}


def get_or_build_pair_index(
    store: SegmentStore,
    embed_fn,
    use_role_prefix: bool = True,
    include_reverse: bool = False,
) -> PairIndex:
    key = (id(store), use_role_prefix, include_reverse)
    if key not in _PAIR_INDEX_REGISTRY:
        _PAIR_INDEX_REGISTRY[key] = PairIndex(
            store,
            embed_fn,
            use_role_prefix=use_role_prefix,
            include_reverse=include_reverse,
        )
    return _PAIR_INDEX_REGISTRY[key]


