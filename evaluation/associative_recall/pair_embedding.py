class PairRolePrefix(_PairBase):
    """(USER: ...\\nASSISTANT: ...) — explicit role labels."""

    arch_name = "pair_roleprefix"
    use_role_prefix = True
    use_v2f = False


class PairNoPrefix(_PairBase):
    """Plain concatenation — user_text + " " + assistant_text."""

    arch_name = "pair_noprefix"
    use_role_prefix = False
    use_v2f = False


class PairPlusV2f(_PairBase):
    """Pair index merged with v2f cue retrievals — intended ship test."""

    arch_name = "pair_plus_v2f"
    use_role_prefix = True
    use_v2f = True


ARCH_CLASSES: dict[str, type] = {
    "pair_roleprefix": PairRolePrefix,
    "pair_noprefix": PairNoPrefix,
    "pair_plus_v2f": PairPlusV2f,
}
