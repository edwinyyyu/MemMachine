class _PairBase(BestshotBase):
    """Base for pair-embedding variants.

    Retrieval:
      1. Main: cosine top-K on raw-turn index.
      2. Secondary: cosine top-M on pair index.
      3. Pair hits promote BOTH constituent turns (score += pair_cos *
         attenuation).
      4. Merge by turn_idx (take max score per turn), rank, truncate to K.
    """

    arch_name: str = "pair_base"
    use_role_prefix: bool = True
    include_reverse: bool = False
    top_m_pairs: int = 10
    top_k_main: int = 50  # raw-turn main index top-K (enough to cover K=50)
    attenuation: float = 0.9
    use_v2f: bool = False  # if True, merge v2f cue retrievals too

    def __init__(self, store: SegmentStore, client: OpenAI | None = None):
        if client is None:
            client = OpenAI(timeout=60.0, max_retries=3)
        super().__init__(store, client)
        self.embedding_cache = PairEmbeddingCache()
        self.llm_cache = PairLLMCache()
        # Build / reuse pair index
        self._pair_index = get_or_build_pair_index(
            store,
            self.embed_text,
            use_role_prefix=self.use_role_prefix,
            include_reverse=self.include_reverse,
        )

    def llm_call(self, prompt: str, model: str = MODEL) -> str:
        cached = self.llm_cache.get(model, prompt)
        if cached is not None:
            self.llm_calls += 1
            return cached
        last_exc: Exception | None = None
        for attempt in range(3):
            try:
                response = self.client.chat.completions.create(
                    model=model,
                    messages=[{"role": "user", "content": prompt}],
                    max_completion_tokens=2000,
                )
                text = response.choices[0].message.content or ""
                self.llm_cache.put(model, prompt, text)
                self.llm_calls += 1
                return text
            except Exception as e:
                last_exc = e
                time.sleep(1.5 * (attempt + 1))
        print(f"    LLM call failed after 3 attempts: {last_exc}", flush=True)
        self.llm_cache.put(model, prompt, "")
        self.llm_calls += 1
        return ""

    def embed_text(self, text: str) -> np.ndarray:
        text = text.strip()
        if not text:
            return np.zeros(1536, dtype=np.float32)
        cached = self.embedding_cache.get(text)
        if cached is not None:
            self.embed_calls += 1
            return cached
        last_exc: Exception | None = None
        for attempt in range(3):
            try:
                response = self.client.embeddings.create(
                    model=EMBED_MODEL, input=[text]
                )
                embedding = np.array(
                    response.data[0].embedding, dtype=np.float32
                )
                self.embedding_cache.put(text, embedding)
                self.embed_calls += 1
                return embedding
            except Exception as e:
                last_exc = e
                time.sleep(1.5 * (attempt + 1))
        print(f"    Embed failed after 3 attempts: {last_exc}", flush=True)
        self.embed_calls += 1
        return np.zeros(1536, dtype=np.float32)

    def _pair_hits_per_role(
        self,
        pair_results: list[tuple[int, float]],
        source_ids: set[int] | None = None,
    ) -> dict[str, int]:
        """For diagnostics: count how many pair-hits came from user-side vs
        assistant-side. (Relative gold is computed outside this function.)
        """
        # Not used at retrieval time; placeholder kept for future extension.
        return {}

    def retrieve(self, question: str, conversation_id: str) -> BestshotResult:
        # 1. Main retrieval: cosine top-K on raw-turn index
        query_emb = self.embed_text(question)
        main_top_k = max(self.top_k_main, 50)
        main_result = self.store.search(
            query_emb, top_k=main_top_k, conversation_id=conversation_id
        )

        # score_map: turn-store-index -> aggregated score (max of all sources)
        score_map: dict[int, float] = {}
        seg_map: dict[int, Segment] = {}
        source_map: dict[int, list[str]] = defaultdict(list)  # diagnostic

        for seg, sc in zip(main_result.segments, main_result.scores):
            score_map[seg.index] = sc
            seg_map[seg.index] = seg
            source_map[seg.index].append(f"main:{sc:.3f}")

        # 2. Secondary pair retrieval
        pair_hits = self._pair_index.search(
            query_emb,
            top_m=self.top_m_pairs,
            conversation_id=conversation_id,
        )

        promoted_ids: list[int] = []  # constituents promoted by pair hits
        pair_scores_each: list[float] = []
        for pair_idx, pair_cos in pair_hits:
            _cid, seg_a_idx, seg_b_idx, _text = self._pair_index.pairs[
                pair_idx
            ]
            promoted_score = pair_cos * self.attenuation
            pair_scores_each.append(pair_cos)
            for seg_idx in (seg_a_idx, seg_b_idx):
                promoted_ids.append(seg_idx)
                # Merge: take max of existing and pair-promoted score
                prev = score_map.get(seg_idx, -1.0)
                if promoted_score > prev:
                    score_map[seg_idx] = promoted_score
                if seg_idx not in seg_map:
                    seg_map[seg_idx] = self.store.segments[seg_idx]
                source_map[seg_idx].append(
                    f"pair{pair_idx}:{pair_cos:.3f}*{self.attenuation}"
                )

        # 3. Optional: v2f cue retrievals merged in
        v2f_cues: list[str] = []
        v2f_output: str = ""
        if self.use_v2f:
            hop0_segments = list(main_result.segments[:10])
            context_section = (
                "RETRIEVED CONVERSATION EXCERPTS SO FAR:\n"
                + _format_segments(hop0_segments)
            )
            prompt = V2F_PROMPT.format(
                question=question, context_section=context_section
            )
            v2f_output = self.llm_call(prompt)
            v2f_cues = _parse_cues(v2f_output)[:2]
            for cue in v2f_cues:
                cue_emb = self.embed_text(cue)
                res = self.store.search(
                    cue_emb, top_k=10, conversation_id=conversation_id
                )
                for seg, sc in zip(res.segments, res.scores):
                    prev = score_map.get(seg.index, -1.0)
                    if sc > prev:
                        score_map[seg.index] = sc
                    if seg.index not in seg_map:
                        seg_map[seg.index] = seg
                    source_map[seg.index].append(f"v2f:{sc:.3f}")

        # 4. Rank by score, truncate
        ranked = sorted(
            score_map.keys(), key=lambda i: score_map[i], reverse=True
        )
        all_segments = [seg_map[i] for i in ranked]

        return BestshotResult(
            segments=all_segments,
            metadata={
                "name": self.arch_name,
                "num_pair_hits": len(pair_hits),
                "pair_scores": [round(s, 4) for s in pair_scores_each],
                "use_role_prefix": self.use_role_prefix,
                "attenuation": self.attenuation,
                "top_m_pairs": self.top_m_pairs,
                "v2f_cues": v2f_cues,
                "v2f_output": v2f_output,
                "pair_promoted_seg_indices": promoted_ids,
                "pair_index_size": len(self._pair_index),
            },
        )


