"""Pair-level Q-A embedding for secondary index retrieval.

At ingest: for each adjacent (user_turn, assistant_turn) pair in a
conversation, concatenate texts and embed the pair as a single unit. This
builds a SECONDARY index, parallel to the raw-turn cosine index.

At retrieval: search both indices, merge. Pair-hits promote BOTH constituent
turns with an attenuated score.

Zero-LLM: no cue generation, just a different embedding grouping. Uses the
validated separate-index + score-bonus pattern.

Variants
  pair_roleprefix   — "USER: {user_text}\\nASSISTANT: {assistant_text}"
  pair_noprefix     — "{user_text} {assistant_text}"
  pair_plus_v2f     — pair index merged with v2f cue retrievals (ship test)

Dedicated caches (pair_*_cache.json) avoid concurrent-agent corruption.
"""

