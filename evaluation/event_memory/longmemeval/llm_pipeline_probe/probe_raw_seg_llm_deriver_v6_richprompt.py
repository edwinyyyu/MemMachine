"""Raw-segmenter + LLM-deriver with v22's RICH prompt content (v6).

What this iterates from v3-dual
-------------------------------

v3-dual matches v22-rawev's dual-text embed format but uses a SHORT
deriver prompt (~40 lines). v3 scored 88.23% vs v22-rawev 89.40% — a
1.17pp gap.

Hypothesis: the gap may be from v22's RICHER ruleset, not from
architectural differences. v22's PROMPT_REWRITE_V22 spells out KEEP/
DROP categories, requires every concrete particular verbatim, has the
DATE-RESOLUTION rule (which we now know is load-bearing), forbids
inferred attributes, etc. v3's prompt skips most of this.

v6 transplants v22's full prompt body into the deriver context. The
deriver still emits a SINGLE dual-text derivative (like v3), but the
rewrite portion is generated by the same rules as v22.

Architecture
------------

Segmenter: RawChunkSegmenter (same as v1/v3/v4/v5)
Deriver: emits one dual-text derivative per segment:
        "<v22-rewrite>\n{speaker}: <raw_chunk>"
        where <v22-rewrite> is generated by v22's PROMPT_REWRITE_V22.

Display: raw events via --answer-with-raw-events
"""

from __future__ import annotations

from typing import override
from uuid import uuid4

from memmachine_server.common.language_model import LanguageModel
from memmachine_server.episodic_memory.event_memory.data_types import (
    Derivative,
    RawSegmentEventContext,
    Segment,
    TextBlock,
)
from memmachine_server.episodic_memory.event_memory.deriver.deriver import (
    Deriver,
)
from pydantic import BaseModel


# Adapted from probe_segmenter_rewrite_v22.PROMPT_REWRITE_V22 but as a
# single-string output (since deriver emits one derivative per segment).
PROMPT_DERIVE_V6_RICH = """\
Rewrite the MESSAGE into a single standalone third-person memory \
statement. The statement is stored verbatim and later retrieved by \
semantic search. A future user querying any specific content in the \
message should find this statement if it contains that content.

KEEP what is specific to this message; DROP what is interchangeable \
across similar messages. Specific content includes: names of people, \
places, organizations, brands, named objects, named activities; \
dates, times, durations, quantities; identifiers, titles, quoted \
phrases, proper nouns; decisions, plans, preferences, opinions, \
relationships, roles, emotional states tied to events; described \
events (something that happened or will happen); attached-media \
descriptions. Interchangeable content has none of these -- bare \
greetings, sign-offs, acknowledgments, reactions, and questions \
whose phrasing introduces nothing from the specific list above. \
Dropping specific content is a FAILURE; emitting a statement for \
interchangeable content is a FAILURE.

THE STATEMENT:
- Corresponds to the EVENT in the message. An event is a single \
occurrence, decision, plan, observation, state, or preference at one \
point or span in time. A multi-sentence elaboration of the same event \
(subject in one sentence, reason in the next, outcome after that) is \
ONE statement that contains all of those sentences' particulars. \
Multiple distinct events in the same message (different times, \
different occasions, different actions) are collapsed into one \
multi-clause statement covering them all -- DO NOT drop a distinct \
event because it is not the message's main event.
- Contains every concrete particular the message gives about its \
event(s) -- subject, action, time, place, attendees, motivation, \
outcome, attached media.
- Refers to the speaker by name. First-person self-references \
resolve to the speaker's name; second-person references resolve to \
the addressee's name when one is known; demonstrative and ambiguous \
pronouns resolve to their concrete referents. The first occurrence \
of any queryable entity is named; subsequent references within the \
statement may use natural pronouns and possessives.
- Preserves every CONCRETE PARTICULAR from the message verbatim -- \
names, dates, numbers, identifiers, decisions, plans, preferences, \
opinions, relationships, emotional states tied to events, \
distinctive phrasing, attached-media descriptions. Generic \
abstractions or stock paraphrases for specifics are FAILURES.
- Preserves polarity, direction, and emotional tone. "Used to" \
implies no longer; "didn't get to bed until 2 AM" implies a late \
end, not a late start.
- Traces every detail back to words in the message. Inferred \
attributes (a title from context, a gender from a name, an age, an \
ethnicity, an unstated role) are FAILURES; explicitly stated \
attributes are preserved.
- Resolves every relative time reference into an absolute date or \
interval anchored at {date}: past-tense and backward markers go \
backward; future-tense and forward markers go forward; "today" / \
"now" resolves to {date}; "this <weekday>" resolves to the nearest \
{date}-occurrence of that weekday; original relative phrasing may \
also remain alongside the resolution.
- Is anchored to the date of the event it describes -- {date} for \
events that happened during the message, or the resolved date for \
events that happened on a different date.
- Reports the content the message conveys, not the speech-act of \
conveying it. "X said that ...", "X told Y that ...", "X mentioned \
that ..." framing is included ONLY when the speech-act itself is \
the event (an apology, a promise, an explicit announcement).

NEIGHBORING TURNS appear before the message strictly to help resolve \
second-person addressees, demonstratives, anaphora, and unresolved \
relative references. Content drawn from the neighbors is NEVER \
emitted -- only content drawn from the message itself.

Output: a JSON object {{ "rewrite": "..." }}. Return an empty string \
``""`` if the message contains no specific content (pure greeting, \
sign-off, "thanks", "lol", etc.).

{neighbors_block}MESSAGE FROM {speaker} on {date}:
{passage}"""


class _DeriveResponse(BaseModel):
    rewrite: str


def _format_neighbors(before) -> str:
    lines: list[str] = []
    if before:
        lines.append("PRIOR TURNS (resolution context only, do not emit):")
        for ev in before:
            lines.append(f"- {ev.producer}: {ev.text}")
        lines.append("")
    return "\n".join(lines) + ("\n" if lines else "")


class RichPromptDualTextDeriver(Deriver):
    """v6 deriver: single dual-text derivative per segment, generated by
    v22-style RICH prompt (KEEP/DROP, every-particular, date-resolved,
    no-speech-acts, no-inferred-attributes).

    Tests whether the v3-to-v22-rawev gap (-1.17pp) closes when the
    deriver prompt is upgraded from v3's short template to v22's full
    ruleset. If yes, prompt richness is the lever. If no, the gap is
    architectural (single-pass segmentation+rewrite vs separated).
    """

    def __init__(
        self,
        *,
        language_model: LanguageModel,
        prompt_template: str = PROMPT_DERIVE_V6_RICH,
        max_attempts: int = 3,
    ) -> None:
        self._language_model = language_model
        self._prompt_template = prompt_template
        self._max_attempts = max_attempts

    @override
    async def derive(self, segment: Segment) -> list[Derivative]:
        if not isinstance(segment.block, TextBlock):
            return []

        if not isinstance(segment.context, RawSegmentEventContext):
            return [
                Derivative(
                    uuid=uuid4(),
                    segment_uuid=segment.uuid,
                    timestamp=segment.timestamp,
                    context=segment.context,
                    block=TextBlock(text=segment.block.text),
                    properties=segment.properties,
                )
            ]

        producer = segment.context.producer
        before = segment.context.before
        event_text = segment.context.current_event_text
        date_str = segment.timestamp.strftime("%Y-%m-%d")
        neighbors_block = _format_neighbors(before)

        prompt = self._prompt_template.format(
            speaker=producer,
            date=date_str,
            passage=event_text,
            neighbors_block=neighbors_block,
        )
        response = await self._language_model.generate_parsed_response(
            output_format=_DeriveResponse,
            user_prompt=prompt,
            max_attempts=self._max_attempts,
        )
        if response is None:
            return []
        rewrite = response.rewrite.strip()
        if not rewrite:
            return []

        raw_chunk = segment.block.text
        dual_text = f"{rewrite}\n{producer}: {raw_chunk}"

        return [
            Derivative(
                uuid=uuid4(),
                segment_uuid=segment.uuid,
                timestamp=segment.timestamp,
                context=segment.context,
                block=TextBlock(text=dual_text),
                properties=segment.properties,
            )
        ]
