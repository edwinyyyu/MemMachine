"""Raw-segmenter + dual-text LLM deriver (v3).

What this iterates from v1
--------------------------

v1 deriver emits multiple short 3p key statements per segment, each
stored as a separate derivative. Embedding for each derivative is
``{speaker}: {3p_statement}`` (via ProducerContext-equivalent format).

v22 baseline's WholeTextDeriver formats each segment's text via
RewriteContext.text_to_embed which is the DUAL-TEXT form:
``{3p_rewrite}\\n{speaker}: {raw_chunk}``. That dual form pairs the
abstract rewrite with the literal raw chunk, giving the embedder BOTH
paraphrase-form retrieval AND literal-phrase retrieval per segment.

v3 mimics v22's dual-text format exactly, but as a single derivative
per segment. The deriver outputs ONE string that IS the dual-text:

    derivative_text = "<3p_rewrite>\\n{speaker}: <raw_chunk>"

Once embedded via _format_with_context (RawSegmentEventContext format
= ``{producer}: {text}``), the final embedding input becomes:

    "{producer}: <3p_rewrite>\\n{speaker}: <raw_chunk>"

(One extra ``{producer}: `` prefix vs v22's exact format. Net diff:
v22 baseline has no producer prefix because it uses RewriteContext
which renders as NullContext-like in formatting.)

Hypothesis: if v3 matches or beats v1 by a clear margin (~2pp), the
dual-text embedding is the structural lever explaining why v1 lost.
If v3 doesn't lift, the dual-text isn't the answer.

Architecture
------------

Segmenter: same RawChunkSegmenter (raw chunk = block.text, no LLM)
Deriver: single dual-text derivative per segment, generated by LLM
        rewriting the segment's full event into 3p. Drops PURE filler
        (defined narrowly: greetings, sign-offs, acknowledgments)
        to keep retrieval pool clean.
"""

from __future__ import annotations

from typing import override
from uuid import uuid4

from memmachine_server.common.language_model import LanguageModel
from memmachine_server.episodic_memory.event_memory.data_types import (
    Derivative,
    RawSegmentEventContext,
    Segment,
    TextBlock,
)
from memmachine_server.episodic_memory.event_memory.deriver.deriver import (
    Deriver,
)
from pydantic import BaseModel


PROMPT_REWRITE_FOR_DERIVATIVE = """\
You are generating a SINGLE retrieval-optimized 3rd-person paraphrase \
of a message. The original message text is stored verbatim alongside \
your paraphrase; both are concatenated as the embedding input. So \
your job: produce a paraphrase that captures the message's specific \
content from a different angle than the verbatim text, helping \
queries match this segment.

OUTPUT a JSON object {{ "rewrite": "..." }}.

The rewrite is a single string -- one or more sentences -- in the \
third-person voice about {speaker}. Resolve first-person ``I``, \
``my``, ``me`` to {speaker}'s name. Resolve second-person ``you`` to \
the addressee's name when known from context. Resolve demonstratives \
to concrete referents.

KEEP every CONCRETE PARTICULAR verbatim -- names, places, dates, \
numbers, identifiers, distinctive phrases, quoted phrases, \
attached-media descriptions. The rewrite must contain every \
queryable particular in the original message.

REPORT content as facts, not as speech acts. Drop ``{speaker} said \
that ...`` / ``{speaker} told X that ...`` wrappers unless the \
speech-act ITSELF is the event (a promise, an apology, an \
explicit announcement, a question whose phrasing IS the searchable \
particular).

DATE AND TIME: keep natural relative phrases verbatim (``yesterday``, \
``next month``, ``three years ago``). Keep explicit absolute dates \
the speaker stated. DO NOT translate ``next month`` to ``April \
2023``. DO NOT add any inline ``[date]``, ``On YYYY-MM-DD``, or \
trailing ``... on YYYY-MM-DD`` anchor the speaker did not state.

DROP PURE FILLER. If the message is ENTIRELY a phatic opener or \
closer (``Hi``, ``Hey``, ``Bye``, ``Take care``, ``Cool!``, ``Lol``, \
standalone ``Yes``/``No``/``Thanks``/``Got it``), output an empty \
string ``""``. The segment will be invisible to retrieval, which is \
correct. CAUTION: a response that CARRIES specific content even if \
short (``Yes, leaving at 5pm Tuesday``, ``Purple was the color I \
picked``, ``I researched adoption agencies``) is NOT pure filler -- \
output a rewrite that includes the specific content.

NEIGHBORING TURNS appear before the message strictly to help resolve \
addressees, demonstratives, anaphora, and unresolved relative \
references. Content drawn from neighbors is NEVER emitted -- only \
content drawn from this message.

{neighbors_block}MESSAGE FROM {speaker} on {date}:
{passage}"""


class _RewriteResponse(BaseModel):
    rewrite: str


def _format_neighbors(before) -> str:
    lines = []
    if before:
        lines.append("PRIOR TURNS (resolution context only, do not emit):")
        for ev in before:
            lines.append(f"- {ev.producer}: {ev.text}")
        lines.append("")
    return "\n".join(lines) + ("\n" if lines else "")


class DualTextLLMRewriteDeriver(Deriver):
    """v3 deriver: emits ONE dual-text derivative per segment, mimicking
    v22 baseline's text_to_embed format.

    Derivative text = "{3p_rewrite}\\n{speaker}: {raw_chunk}"
    Embedded as "{producer}: {3p_rewrite}\\n{speaker}: {raw_chunk}"
    """

    def __init__(
        self,
        *,
        language_model: LanguageModel,
        prompt_template: str = PROMPT_REWRITE_FOR_DERIVATIVE,
        max_attempts: int = 3,
    ) -> None:
        self._language_model = language_model
        self._prompt_template = prompt_template
        self._max_attempts = max_attempts

    @override
    async def derive(self, segment: Segment) -> list[Derivative]:
        if not isinstance(segment.block, TextBlock):
            return []

        if not isinstance(segment.context, RawSegmentEventContext):
            # Fallback for non-experimental contexts: pass-through.
            return [
                Derivative(
                    uuid=uuid4(),
                    segment_uuid=segment.uuid,
                    timestamp=segment.timestamp,
                    context=segment.context,
                    block=TextBlock(text=segment.block.text),
                    properties=segment.properties,
                )
            ]

        producer = segment.context.producer
        before = segment.context.before
        event_text = segment.context.current_event_text
        date_str = segment.timestamp.strftime("%Y-%m-%d")
        neighbors_block = _format_neighbors(before)

        prompt = self._prompt_template.format(
            speaker=producer,
            date=date_str,
            passage=event_text,
            neighbors_block=neighbors_block,
        )
        response = await self._language_model.generate_parsed_response(
            output_format=_RewriteResponse,
            user_prompt=prompt,
            max_attempts=self._max_attempts,
        )
        if response is None:
            return []
        rewrite = response.rewrite.strip()
        if not rewrite:
            # Pure filler -> no derivative -> segment invisible to retrieval.
            return []

        # Dual-text format mimics v22 baseline's text_to_embed exactly:
        # "{3p_rewrite}\n{speaker}: {raw_chunk}"
        raw_chunk = segment.block.text
        dual_text = f"{rewrite}\n{producer}: {raw_chunk}"

        return [
            Derivative(
                uuid=uuid4(),
                segment_uuid=segment.uuid,
                timestamp=segment.timestamp,
                context=segment.context,
                block=TextBlock(text=dual_text),
                properties=segment.properties,
            )
        ]
